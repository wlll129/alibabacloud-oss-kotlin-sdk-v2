    public fun clearMimeType() {
        userMappings.clear()
    }

    @Synchronized
    public fun addMimeType(maps: Map<String, String>) {
        userMappings.putAll(maps)
    }

    public fun getMimetype(name: String): String? {
        return getOrDefault(getNameExt(name), null)
    }

    public fun getMimetype(name: String, defaultValue: String?): String? {
        return getOrDefault(getNameExt(name), defaultValue)
    }

    private fun getOrDefault(ext: String?, defaultValue: String?): String? {
        return ext?.let { userMappings[it] ?: defaultMappings[it] } ?: defaultValue
    }

    private fun getNameExt(fileName: String): String? {
        val lastPeriodIndex = fileName.lastIndexOf(".")
        if (lastPeriodIndex > 0 && lastPeriodIndex + 1 < fileName.length) {
            return fileName.substring(lastPeriodIndex).lowercase(Locale.getDefault())
        }
        return null
    }
